    public void relojDigital(){
        Timer timerHoraFecha = new Timer (1000, (ActionEvent e) -> {
            lblHoraActual.setText(mostrarHora());
            lblFechaActual.setText(mostrarFecha());
        });
        Timer timerTexto = new Timer (10000, (ActionEvent e) -> {
            if(lblBienvenido.getText().equalsIgnoreCase("Bienvenido")){
                if(fechaActual.getHours() >= 6 && fechaActual.getHours() <= 12){
                    lblBienvenido.setText("Buen DÃ­a");
                }else if(fechaActual.getHours() >= 13 && fechaActual.getHours() <= 18){
                    lblBienvenido.setText("Buena Tarde");
                }else if(fechaActual.getHours() >= 19 && fechaActual.getHours() <= 23){
                    lblBienvenido.setText("Buena Noche");
                }else if(fechaActual.getHours() >= 0 && fechaActual.getHours() <= 5){
                    lblBienvenido.setText("Buen Trasnoche");
                }
            }else lblBienvenido.setText("Bienvenido");
        });
        timerHoraFecha.start();
        timerTexto.start();
    }
    
    public String mostrarHora(){
        String hora = String.format("%TT", new java.util.Date());
        return hora;
    } 
    
    public String mostrarFecha(){
        SimpleDateFormat formatoFecha = new SimpleDateFormat("EEEE d 'de' MMMM 'de' yyyy",
        new Locale("ES", "PE")); 
        String fecha = formatoFecha.format(fechaActual);
        return fecha;
    } 
    
