    public void buscarContratosActivos(){
        try{
            gestionarContrato = new ServGestionarContrato();
            listaContrato = gestionarContrato.buscarContratosActivos();     
        }catch (Exception e) {   
            Mensaje.mostrarError(null, Mensaje.ERROR_SERVICIO + "BuscarContratosActivos()");            
        }
    }
    
    public void actualizarEstadoContratos(){
        Timer timerActualizaEstado = new Timer (10000, (ActionEvent e) -> {
            lblActualizando.setText("");
            buscarContratosActivos();
            if(!listaContrato.isEmpty()){
                for(Contrato objetoContrato : listaContrato){
                    if(objetoContrato.getFechafincontrato().getTime() < fechaActual.getTime()){
                        lblActualizando.setText("Actualizando...");
                        try {
                            gestionarContrato = new ServGestionarContrato();
                            gestionarContrato.actualizarEstadoContrato(objetoContrato);
                        } catch (Exception ex) {
                            Mensaje.mostrarError(null, Mensaje.ERROR_SERVICIO + "actualizarEstadoContratos()");  
                        }
                    }
                }
            }
        });    
        timerActualizaEstado.start();
    }
    
